import { chromium } from "playwright";
const BASE = "http://localhost:3100";
const OUT = "/Users/juangarces/dev/Vecinity/vecinity-app/docs/manual/screens";
const browser = await chromium.launch();
const ctx = await browser.newContext({ viewport: { width: 390, height: 844 }, deviceScaleFactor: 2 });
const page = await ctx.newPage();
await page.goto(BASE + "/login", { waitUntil: "networkidle" });
await page.getByPlaceholder("Correo electrónico").fill("juanperez@cantera.test");
await page.getByPlaceholder("Contraseña").fill(process.env.DEMO_VECINO_PASS);
await page.getByRole("button", { name: "Entrar" }).click();
await page.waitForFunction(() => location.pathname !== "/login", { timeout: 25000 });
await page.goto(BASE + "/dashboard/comite", { waitUntil: "networkidle" });
await page.getByRole("button", { name: /Puerta peatonal/ }).click();
await page.waitForFunction(() => document.body.innerText.includes("EN VIVO"), { timeout: 30000 });
await page.waitForFunction(() => document.body.innerText.includes("Últimas aperturas"), { timeout: 15000 });
await page.waitForTimeout(500);
const card = page.locator("section", { hasText: "Puerta peatonal" }).last();
await card.screenshot({ path: `${OUT}/36-comite-camara-bitacora.png` });
console.log("✓ 36-comite-camara-bitacora");
await browser.close();
